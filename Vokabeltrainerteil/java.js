let selectedChapters = [];
let currentQuestionIndex = 0;
let currentLanguage = 'de-fr';
let questionOrder = [];
let currentChapterIndex = 0;

function handleSelection() {
    const selection = document.getElementById("selection").value;
    const message = document.getElementById("message");
    const chapterForm = document.getElementById("chapterForm");
    const chapters7 = document.getElementById("chapters-7");
    const chapters8 = document.getElementById("chapters-8");

    if (selection === "5" || selection === "6" || selection === "7") {
        message.textContent = "Es kommen noch mehr Fragen.";
        setTimeout(() => message.textContent = "", 3000);
        chapterForm.style.display = "none"; // Kapitel-Auswahl ausblenden
    } else if (selection === "7") {
        chapterForm.style.display = "block";
        chapters7.style.display = "block"; // Unités für 7. Klasse anzeigen
        chapters8.style.display = "none"; // Unités für 8. Klasse ausblenden
        message.textContent = "";
    } else if (selection === "8") {
        chapterForm.style.display = "block";
        chapters7.style.display = "none"; // Unités für 7. Klasse ausblenden
        chapters8.style.display = "block"; // Unités für 8. Klasse anzeigen
        message.textContent = "";
    } else {
        chapterForm.style.display = "none";
        message.textContent = "";
    }
}

function startQuiz() {
    const checkboxes = document.querySelectorAll(".chapter-box input[type='checkbox']");
    selectedChapters = [];
    checkboxes.forEach(checkbox => {
        if (checkbox.checked) {
            selectedChapters.push(parseInt(checkbox.value));
        }
    });

    if (selectedChapters.length === 0) {
        document.getElementById("error-message").textContent = "Bitte wähle mindestens ein Kapitel aus.";
        return;
    }

    currentQuestionIndex = 0;
    currentChapterIndex = 0;
    loadChapterQuestions(); // Lade die Fragen für das erste Kapitel
    document.getElementById("chapterForm").style.display = "none";
    document.getElementById("quiz-container").style.display = "block";
    loadQuestion();
}

function loadChapterQuestions() {
    // Frage-Reihenfolge für das aktuelle Kapitel basierend auf dem Index setzen und mischen
    const chapterId = selectedChapters[currentChapterIndex];
    questionOrder = Object.keys(units[chapterId].words);
    questionOrder.sort(() => Math.random() - 0.5);
}

function loadQuestion() {
    const questionElement = document.getElementById("question");
    currentLanguage = document.getElementById("languageDirection").value;

    if (currentQuestionIndex < questionOrder.length) {
        const wordKey = questionOrder[currentQuestionIndex];
        const word = units[selectedChapters[currentChapterIndex]].words[wordKey];

        questionElement.textContent = currentLanguage === 'de-fr' ? word.de.join(", ") : word.fr.join(", ");
        document.getElementById("answer").value = "";
    } else if (currentChapterIndex < selectedChapters.length - 1) {
        currentChapterIndex++;
        currentQuestionIndex = 0;
        loadChapterQuestions(); // Nächstes Kapitel laden
        loadQuestion();
    } else {
        endQuiz();
    }
}

function checkAnswer() {
    const answer = document.getElementById("answer").value.trim().toLowerCase();
    const feedbackElement = document.getElementById("feedback");
    const wordKey = questionOrder[currentQuestionIndex];
    const correctAnswers = currentLanguage === 'de-fr' ? units[selectedChapters[currentChapterIndex]].words[wordKey].fr : units[selectedChapters[currentChapterIndex]].words[wordKey].de;

    const isCorrect = correctAnswers.some(correct => answer === correct.toLowerCase());

    if (isCorrect) {
        feedbackElement.textContent = "Richtig!";
    } else {
        feedbackElement.textContent = `Falsch! Die richtige Antwort ist: ${correctAnswers.join(', ')}`;
    }

    setTimeout(() => {
        feedbackElement.textContent = "";
        currentQuestionIndex++;
        loadQuestion();
    }, 2000);
}

function endQuiz() {
    document.getElementById("quiz-container").style.display = "none";
    const unitStatus = document.getElementById("unit-status");
    unitStatus.innerHTML = `
        <h3>Quiz beendet!</h3>
        <button onclick="goBackToSelection()">Zurück zur Auswahl</button>
    `;
}

function goBackToSelection() {
    document.getElementById("quiz-container").style.display = "none";
    document.getElementById("chapterForm").style.display = "block";
    document.getElementById("unit-status").innerHTML = "";     
}
